let input_a = Number(prompt(`Mời nhập số a: `))
let input_b = Number(prompt(`Mời nhập số b: `))

if (input_a > input_b) {
    alert(`Số a = ${input_a} lớn hơn số b = ${input_b}`)
}
else if (input_b > input_a){
    alert(`Số b = ${input_b} lớn hơn số a = ${input_a}`)
}

else if(input_a = input_b) {
    alert(`Số a = ${input_a} bằng số b = ${input_b}`)
}
